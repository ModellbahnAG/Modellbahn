/**
* @mainpage Anleitung für die Erstellung und Verwaltung von Arduino Projekten
*
* @section Einleitung
* Um die Erstellung und Verwaltung von Arduino Programmen nutzen wir ein
* Konzept, welches ich euch hier erkläre.
*
* @section Entwicklungsumgebung
* Wenn ihr euch entschlossen habt mit dem Arduino zu arbeiten, kennt ihr
* bestimmt schon die Arduino IDE. Für einfache Projekte ist sie sehr gut
* geeignet, da sie sehr leicht zu bedienen ist.
* <p>
* Wir nutzen hier allerdings ein anderes Programm.
* <a href="http://platformio.org"> PlatformIO </a> bietet viele nützliche
* Funktionen für Fortgeschrittene, behält dabei allerdings eine
* einfache Bedienbarkeit bei, sodass auch Anfänger sich darin schnell
* zurechtfinden.
*
* @section Softwareverwaltung
* Verschiedene Versionen seiner Programme zu behalten indem man sie mit
* verschiedenen Namen speichert funktioniert, ist aber nicht wirklich schön.
* Außerdem gibt es bessere Wege um die geschriebenen Programme zwischen
* verschiedenen Computern zu teilen als sie auf einem Stick zu speichern. Einer
* dieser Wege wäre es die Programme in einer Cloud zu speichern, allerdings wäre
* dabei noch nicht das Problem der verschiedenen Versionen gelöst.
* <p>
* Zu userem Glück existiert bereits eine Lösung für das Problem der
* verschiedenen Versionen. Dieses nennt sich Git und ist ein Werkzzeug zur
* Versionsverwaltung. Jetzt stellt sich nur die Frage, wie wir Git in eine Cloud
* bekommen, aber auch hierfür gibt es genügend Anbieter. Einer der verbreitesten
* ist <a href="http://github.com">Github</a>, welches wir also auch verwenden
* werden.
*/
